function sum(num1, num2){
    return num1 + num2;
}
function minus(num1, num2) {
    return num1 - num2;
}
function multiplication(num1, num2) {
    return num1 * num2;
}
function divison(num1, num2) {
    return num1 / num2;
}
function degree(num1, num2) {
    return num1 ** num2;
}
function percent(num1, num2) {
    return num1 % num2;
}
function root(num1, num2) {
    return num1 ** (1/num2);
}
